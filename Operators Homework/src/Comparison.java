
//Write a java program to compare two numbers using ==, !=, >, <, >=, <=. and display result
public class Comparison {
	public static void main(String args[]) {
	int a = 15;
	int b = 19;
	
	System.out.println(a + " == " + b + "  : " + (a == b));
	System.out.println(a + " != " + b + "  : " + (a != b));
	System.out.println(a + " > " + b + "  : " + (a > b));
	System.out.println(a + " < " + b + "  : " + (a < b));
	System.out.println(a + " >= " + b + "  : " + (a >= b));
	System.out.println(a + " <= " + b + "  : " + (a <= b));
}
}